           <h3>Teaching/Research Experience:</h3>
           <p><br>
Dr. Sangeeta Chauhan, M.A. (English), M.Ed., M.Phil, Ph.D from Department of Education (CIE), University of Delhi , has teaching and research experience spread over 30 years.

Prior to this she was consultant in the University since April 2006 , wherein she was also entrusted with the task of starting the M.Ed programme in the campus . During this period she organized eight Professional Development Workshops for B.Ed Teacher Educators of affiliated institutions and two workshops for the Principals of the B.Ed colleges. One national seminar on “Technology Integration in Education” sponsored by Intel® Teach was organized by her . A book ‘Sankalan' comprising the award winning lesson plans, edited by her and sponsored by the University, was released this year at the inaugural ceremony of the M.Ed programme at the University. She also organized a B.Ed syllabus revision workshop during this period.

Workshops regarding the formulation of the M.Ed syllabus were organized. The M.Ed programme has incorporated this syllabus which is very different from other Universities as it has Technology integrated in the Curriculum and practicals included for each paper (core and elective).

She was Principal of CES College of Educational Research and Training (B.Ed. College) affiliated to GGS Indraprastha University and Coordinator of affiliated B.Ed colleges of I. P. University for over 6 years. Since 1999 she had also been teaching Educational Psychology, Methods Of Teaching English, Special Education at B.Ed level. She organized Teacher Education Workshops for secondary school teachers at CESCERT (18 in number) from 1999 to 2006. She organized Academic Workshops and Faculty development Programmes, two national seminars, six annual days of the college and supervised 50 action researches during this period.

She also coordinated Jamia Millia Islamia distance B.Ed programme for two years.

She taught Philosophy, Psychology and History of Education both at graduate and B.El.Ed. level at Shyama Prasad Mukherjee College between 1992 and 1999. Simultaneously she took guest lectures regularly at Aditi Mahavidyalya, Delhi University between 1996 and 1998. She participated in many seminars and organized events as member of various committees of the college.

She was a Junior and then Senior Research Fellow from 1987 to 1991 (University Grants Commission). During that period she edited and co-edited Ph.D and M.Phil abstracts for CIE and Delhi University .

She was also granted the UGC scholarship for pursuing M.Phil and Ph.D degree.

She took Psychology classes at B.Ed level at Department of Education (CIE) from 1985 and 1987.

She worked in Indian Council of Social Science and Research (ICSSR) project in “ The comparative study between the researches carried out in CIE, Delhi University and Jamia Millia Islamia ” from 1984 to 1985.

She worked in the Department of Chinese and Japanese studies, Delhi University in a project “World wide research on the image of Japan ” from 1983 to 1984.

She worked in the child guidance centre, Department of Social Works, Delhi University from 1982 to 1983. Wherein she was giving diagnostic treatment to children having behaviour, emotional, educational and speech problems.

She has presented papers at various conferences and seminars at national level (IATE, Gnostic Centre, Jeewan Vigyan, MACASE, CIE, JMI and B.Ed affiliated institutes).

She has been on air on several interactive radio counselling programmes of Gyan Vani relating to concerns and issues of teacher education programmes.

She has been invited speaker for several seminars, a few times at national level too.

She is a life member of Indian Association of Teacher Educators and

All India Association for Educational Research .

She has 5 Research Scholars pursuing Ph.D under her supervision.

She also has one Scholar pursuing Post Doctoral Research under her for a UGC sponsored project.

She was awarded Gold Medal for her work by the Ministry of Education.

