<p><strong>Membership Rules: </strong></p>
<ul><li> The membership of the Association shall be restricted to the persons who have obtained degree and are not enrolled with the university school of Information Technology . </p>
<li> There shall be two types of membership of the association: </p>
<ol> <li> <strong>Patrons </strong></p>
<ul><li> Any distinguished person/persons may be associated as patron/patrons of the association through a unanimous resolution of the governing body. A patron shall be entitled to observe the functioning of the society and give his suggestions for the development of the associations. </p>
<li> Patron shall not have voting rights. </p>
</ul><li> <strong>Life membership </strong></p>
<ul><li> Any person who pays sum not less than Rs. 1000/- as may be decided by the governing body from time to time and dedicates himself to the attainment of the aims and objectives of the association and has obtained degree from the university school of Information Technology is eligible for the life membership of the association. </p>
</ul></ol></ul>

<p><strong>Termination of Membership: </strong></p>
<ol type="a"><li> The governing body of the association shall have the power to expel any member by termination of his membership any of the following grounds: </p>
<ol type="i"><li>Death of member 
  </p>
<li>Working against the aims and objectives of the association. 
  </p>
<li>Resignation from membership. 
  </p>
<li>Failure to attend five meetings of the general body continuously without information. 
  </p>
<li>Indiscipline or misconduct. 
  </p>
</ol><li> Before the termination of membership any member, he shall be given an opportunity of being heard and reasons for termination of membership shall be communicative to him in writing. </p>
<li> An appeal against any such decision shall lie before the general body. </p>
</ol>
