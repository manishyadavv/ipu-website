    <h1 align="center" ><strong>DOCTORAL RESEARCH</strong></h1>
		<p>
		The University, since its inception, is also providing facility to the students to pursue research leading to the award of the degree of Doctor of Philosophy (Ph.D) in all the University Schools of Studies and in all professional disciplines.

The University has recently taken the lead in recasting the University's ‘Ph.D. Ordinance' and the ‘Regulation in regard to Approved Research Centres and Approved Supervisors' and the ‘Amended University's Ordinance' is in alignment with the recently issued UGC's Regulations (minimum Standards and Procedure for the Award of M.Phil./Ph.D. Degree), 2009 ; the Amended Ph.D. Ordinance has an accredited system of registering Ph.D. scholars via conducting an entrance test/ personal nterview, meeting the compulsory courseware requirement, etc.

While, it does provide opportunities for professionals and administrators working in corporate sector/ government institutions to undertake doctoral research, there are adequate quality checks on doctoral research such as qualifications for supervisors, performance monitoring of scholars, publications in referred journals, etc. To this, we may add that no Research Scholar is allowed to submit his Ph.D. thesis unless he/she has published minimum of 2 research papers in international (referred) journals.
